#include <iostream>
#include "BigInt.h"

using namespace std;

/*******************************************************************************
* Main
*******************************************************************************/
int main()
{
	for (int a = 2; a <= 100; a++)
	{
		BigInt product(a);
		for (int b = 2; b <= 100; b++)
		{
			product *= a;
			cout << a << "^" << b << " = " << product << endl;
		}
	}

	return 0;
}