#include <iostream>

using namespace std;
 
/******************************************************************************
* Main
******************************************************************************/
int main()
{
	for (int i = 11; i < 100; i++)
	{
		for (int j = 11; j < i; j++)
		{
			int i1  = i % 10;
			int i10 = i / 10;
			int j1  = j % 10;
			int j10 = j / 10;

			if (i1 != 0 && i10 != 0 && j1 != 0 && j10 != 0)
			{
				if (i1 == j1)
				{
					if (j * i10 == i * j10)
					{
						cout << j << "/" << i << endl;
					}
				}
				if (i1 == j10)
				{
					if (j * i10 == i * j1)
					{
						cout << j << "/" << i << endl;
					}
				}
				if (i10 == j1)
				{
					if (j * i1 == i * j10)
					{
						cout << j << "/" << i << endl;
					}
				}
				if (i10 == j10)
				{
					if (j * i1 == i * j1)
					{
						cout << j << "/" << i << endl;
					}
				}
			}
		}
	}

	return 0;
}